def readfile(f):
    line = f.read()
    line = line.lower()
    line = line.strip()
    new = ""
    for char in line:
        if char not in punctuations:
            new = new+char
    words = new.split(" ")
    return words


def wordCount(words):
    d = dict()
    for c in words:
        if c not in d:
            d[c] = 1
        else:
            d[c] += 1
    return d


def topTenWords(d):
    rd = dict(sorted(d.items(), key=operator.itemgetter(1),reverse=True))
    print("Top 10 Words")
    out = dict(list(rd.items())[0: 10])
    print(out)
    print(" Top 10 Words ignoring stop words")
    c = 0;
    for key in list(rd.keys()):
        if (c==10): break
        if key not in stopwords:
            print(key,":",rd[key])
            c+=1
    
    

    

